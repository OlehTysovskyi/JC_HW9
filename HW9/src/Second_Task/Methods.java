package Second_Task;

public class Methods {
	
	public static void dod(double a, double b) {
		double sum = a + b;
		System.out.println("a+b= " + sum + ";");
	}
	
	public static void vid(double a, double b) {
		double dif = a - b;
		System.out.println("a-b= " + dif + ";");
	}
	
	public static void mnz(double a, double b) {
		double rez = a + b;
		System.out.println("a*b= " + rez + ";");
	}
	
	public static void dil(double a, double b) {
		double frc = a + b;
		System.out.println("a/b= " + frc + ";");
	}
		
}
